#  Read the input file
data <- read.table ("timing.dat")

latencies <- ((data$V4 - data$V3) / 2) / data$V2
latencies <- round (latencies, digits = 3)

data_frame <- data.frame (data$V1, data$V2, latencies)
write.table (data_frame, file = "latency_agg.dat", col.names = FALSE, row.names = FALSE)

